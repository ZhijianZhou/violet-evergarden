def calculate_scores(answers: Dict[str, str]) -> Dict:
    """
    è®¡ç®—æ‰€æœ‰ç»´åº¦çš„åˆ†æ•°
    """
    dimensions = {
        "energy_source": ENERGY_SOURCE,
        "information": INFORMATION,
        "decision": DECISION,
        "lifestyle": LIFESTYLE
    }
    
    results = {}
    
    for dim_name, dim_data in dimensions.items():
        scores = {"A": 0, "B": 0}  # A = ç¬¬ä¸€ä¸ªé€‰é¡¹, B = ç¬¬äºŒä¸ªé€‰é¡¹
        total = 0
        
        for q in dim_data["questions"]:
            if q["id"] in answers:
                if answers[q["id"]] == q["options"][0][0]:
                    scores["A"] += 1
                else:
                    scores["B"] += 1
                total += 1
        
        if total > 0:
            # è½¬æ¢ä¸ºç™¾åˆ†æ¯” (Açš„å€¾å‘)
            pct_A = (scores["A"] / total) * 100
            results[dim_name] = {
                "A_percentage": pct_A,
                "B_percentage": 100 - pct_A,
                "score_A": scores["A"],
                "score_B": scores["B"]
            }
        else:
            results[dim_name] = {"A_percentage": 50, "B_percentage": 50, "score_A": 0, "score_B": 0}
    
    return results


def get_type_from_scores(scores: Dict) -> str:
    """
    æ ¹æ®åˆ†æ•°ç¡®å®š MBTI ç±»å‹
    """
    type_letters = []
    
    # E/I
    if scores["energy_source"]["A_percentage"] > 50:
        type_letters.append("E")
    else:
        type_letters.append("I")
    
    # S/N
    if scores["information"]["A_percentage"] > 50:
        type_letters.append("S")
    else:
        type_letters.append("N")
    
    # T/F
    if scores["decision"]["A_percentage"] > 50:
        type_letters.append("T")
    else:
        type_letters.append("F")
    
    # J/P
    if scores["lifestyle"]["A_percentage"] > 50:
        type_letters.append("J")
    else:
        type_letters.append("P")
    
    return "".join(type_letters)


